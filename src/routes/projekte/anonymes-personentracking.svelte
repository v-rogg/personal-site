<h4>
    Obacht!
</h4>
<p>
    Das anonyme Personentracking Obacht! ist ein Gegenprodukt zur weit verbreiteten, jedoch moralisch fragwürdigen, Gesichtserkennung.
    Das System besteht aus einer Hardwarekomponente, zuständig für das Tracking, sowie aus drei voneinander unabhängigen, jedoch miteinander kombinierbaren, Software-Modulen:<br />
    Obacht! Analytics, Obacht! Security und Obacht! Interaction.
    <br /><br />
    Der Fokus von Obacht! liegt auf der Erfassung, Auswertung und Interaktion von Bewegungs- und Verhaltensmustern in Echtzeit.
    Je nach Verwendungszweck verfolgt Obacht! unterschiedliche Ziele; jedoch steht immer im Vordergrund, einen Mehrwert zu schaffen, ohne dafür die Privatsphäre der Zielpersonen zu gefährden.
    Um dies zu gewährleisten garantieren die verbauten LiDAR-Sensoren bereits auf Hardwareebene 100% Anonymität.
    Aus diesem Grund kann Obacht! im privaten, sowie im öffentlichen Raum, ohne Probleme eingesetzt werden.
    Darüber hinaus funktionieren die Sensoren unbeeinflusst von äußerem Einwirkungen, wie äußeren Lichtverhältnissen und Witterungsbedingungen.
    <br /><br />
    Die Software-Module sind als Web-Applikation realisiert, bieten einen Gesamtüberblick über das System, und werten die gesammelten Daten je nach Anwendungszweck speziell auf.
</p>
