<h4>
    Obacht!
</h4>
<p>
    Das anonyme Personentracking Obacht! verfolgt einen gegensätzlichen Ansatz zur weit verbreiteten, jedoch moralisch fragwürdigen, Gesichtserkennung. Der Fokus von Obacht! liegt auf der Erfassung, Auswertung und Interaktion von Bewegungs- und Verhaltensmustern.
    <br /><br />
    Je nach Verwendungszweck verfolgt Obacht! unterschiedliche Ziele; jedoch steht immer im Vordergrund, einen Mehrwert zu schaffen, ohne dafür die Privatsphäre der Zielpersonen zu gefährden.
    Um dies zu gewährleisten garantieren die verbauten LiDAR-Sensoren bereits auf Hardwareebene 100% Anonymität. Aus diesem Grund kann Obacht! im privaten, sowie im öffentlichen Raum, ohne Probleme eingesetzt werden.
    Darüber hinaus funktionieren die Sensoren unbeeinflusst von äußerem Einwirkungen, wie Lichtverhältnissen und Witterungsbedingungen.
    Eine Web-Applikation bieten einen Gesamtüberblick über das System, und visualisiert die gesammelten Daten je nach Anwendungszweck speziell.
</p>
